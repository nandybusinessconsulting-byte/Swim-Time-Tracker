function t(str: string): number {
  const parts = str.split(':');
  if (parts.length === 2) {
    const mins = parseInt(parts[0], 10);
    const [secStr, centStr = '00'] = parts[1].split('.');
    const secs = parseInt(secStr, 10);
    const cents = parseInt(centStr.padEnd(2, '0'), 10);
    return (mins * 60 + secs) * 100 + cents;
  }
  const [secStr, centStr = '00'] = str.split('.');
  const secs = parseInt(secStr, 10);
  const cents = parseInt(centStr.padEnd(2, '0'), 10);
  return secs * 100 + cents;
}

