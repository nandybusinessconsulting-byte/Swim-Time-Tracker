export const STANDARDS: StandardEntry[] = RAW.flatMap(row =>
  row.times.map((timeHundredths, i) => ({
    ageGroup: row.ag,
    gender: row.g,
    courseType: SCY,
    eventId: row.ev,
    level: LEVELS[i],
    timeHundredths,
    season: SEASON,
  }))
);

export function getStandard(
  ageGroup: AgeGroup,
  gender: Gender,
  eventId: string,
  level: StandardLevel,
  courseType: CourseType = 'SCY'
): StandardEntry | undefined {
  return STANDARDS.find(
    s =>
      s.ageGroup === ageGroup &&
      s.gender === gender &&
      s.eventId === eventId &&
      s.level === level &&
      s.courseType === courseType
  );
}

export function getStandardsForEvent(
  ageGroup: AgeGroup,
  gender: Gender,
  eventId: string,
  courseType: CourseType = 'SCY'
): Partial<Record<StandardLevel, number>> {
  const result: Partial<Record<StandardLevel, number>> = {};
  STANDARD_LEVELS.forEach(({ level }) => {
    const entry = getStandard(ageGroup, gender, eventId, level, courseType);
    if (entry) result[level] = entry.timeHundredths;
  });
  return result;
}
